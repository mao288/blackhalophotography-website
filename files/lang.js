// ===== LANGUAGE ENGINE =====
function getLang() {
  return localStorage.getItem('bh-lang') || 'es';
}

function setLang(lang) {
  localStorage.setItem('bh-lang', lang);
  applyLang(lang);
  updateToggleUI(lang);
}

function applyLang(lang) {
  document.documentElement.lang = lang;

  // Text elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (translations[key] && translations[key][lang]) {
      // Check if it's an input/textarea placeholder or select
      if (el.tagName === 'LABEL' || el.tagName === 'OPTION') {
        el.textContent = translations[key][lang];
      } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[key][lang];
      } else {
        el.innerHTML = translations[key][lang];
      }
    }
  });

  // Update aria-label on toggle
  const toggle = document.querySelector('.lang-toggle');
  if (toggle) {
    toggle.setAttribute('aria-label', lang === 'es' ? 'Switch to English' : 'Cambiar a Español');
  }
}

function updateToggleUI(lang) {
  document.querySelectorAll('.lang-opt').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
}

function toggleLang() {
  const current = getLang();
  setLang(current === 'es' ? 'en' : 'es');
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  const lang = getLang();
  applyLang(lang);
  updateToggleUI(lang);
});
